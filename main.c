#include <stdio.h>


int main()
{

//---------------------------------------------------
    int x;
    printf("enter a five digit integer : ");
    scanf("%d",&x);

    printf( "%d +",(x      -x%10000)/10000);
    printf(" %d +",(x%10000-x%1000 )/1000 );
    printf(" %d +",(x%1000 -x%100  )/100  );
    printf(" %d +",(x%100  -x%10   )/10   );
    printf(" %d =",(x%10   -0      )/1    );

    printf(" %d",(x      -x%10000)/10000  // d0000
                +(x%10000-x%1000 )/1000   // 0d000
                +(x%1000 -x%100  )/100    // 00d00
                +(x%100  -x%10   )/10     // 000d0
                +(x%10   -0      )/1      // 0000d
                                         );
    return 1;
}
